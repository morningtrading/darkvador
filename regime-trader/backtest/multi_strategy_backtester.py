"""
multi_strategy_backtester.py — Walk-forward backtester for multiple parallel strategies.

Each strategy
  - Has its own HMM engine and StrategyOrchestrator trained on its symbol universe
  - Operates in an isolated sub-portfolio (its own cash + shares)
  - Receives capital from CapitalAllocator (rebalanced on a configurable interval)
  - Has every signal validated by PortfolioRiskManager before execution

Portfolio equity = sum of all strategy sub-portfolio equities.

CAPITAL MODEL
-------------
Portfolio has a shared reserve pool of cash.  Each strategy sub-portfolio starts
with initial_capital / n_strategies (before first allocator rebalance).

Allocator rebalance transfers cash between sub-portfolios by adjusting each
strategy's cash balance to match the target capital.  If a strategy is
over-weight, excess cash flows back to the reserve pool; if under-weight,
cash is pulled from the reserve.  This is a backtest simplification — live
trading would require liquidating positions to fund rebalancing.

SIGNAL SCALING FOR PRM
-----------------------
StrategyOrchestrator returns signals with position_size_pct relative to the
strategy's sub-portfolio.  Before PRM validation the signal is scaled to a
portfolio-relative fraction so PRM checks (aggregate exposure, symbol
concentration, leverage) operate on the same basis.

  portfolio_pct = signal.position_size_pct × leverage × (strat_equity / total_equity)

After PRM approves (possibly reduced), the approved portfolio_pct is unscaled
back to strategy-relative for execution against the sub-portfolio equity.

BENCHMARK COMPARISONS
---------------------
When run_benchmarks=True, three additional equity curves are generated:
  equal_weight  — same strategies, CapitalAllocator(equal_weight)
  best_single   — the single strategy with highest Calmar run alone
  buy_and_hold  — buy-and-hold of the most-traded symbol from bar 1

CLI
---
python main.py backtest --multi-strat --start 2020-01-01 --end 2024-12-31 --compare
"""

