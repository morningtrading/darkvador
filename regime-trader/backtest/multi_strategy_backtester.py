@dataclass
class StrategySpec:
    """
    Configuration for one strategy in a multi-strategy run.

    Parameters
    ----------
    name            : Unique identifier used throughout results and logging.
    symbols         : Symbols this strategy trades.
    hmm_config      : HMM hyperparameter overrides (merged with defaults).
    strategy_config : StrategyOrchestrator config overrides.
    weight_min      : Minimum allocator weight for this strategy (fraction of total capital).
    weight_max      : Maximum allocator weight.
    hmm_symbol      : Symbol used to build HMM features.  Defaults to symbols[0].
    """

    name: str
    symbols: List[str]
    hmm_config: Optional[Dict] = None
    strategy_config: Optional[Dict] = None
    weight_min: float = 0.0
    weight_max: float = 1.0
    hmm_symbol: Optional[str] = None   # defaults to symbols[0] if None

    def primary_symbol(self) -> str:
        return self.hmm_symbol or self.symbols[0]


